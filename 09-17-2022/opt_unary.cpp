#include <iostream>
using namespace std;

int main(int argc, char **argv) {

    // Variable
    int a = 5;

    // Output
    cout << "Nilai A Positif = " << +a <<endl;
    cout << "Nilai A Negatif = " << -a <<endl;
    cout << "Nilai A Menaik = " << ++a <<endl;
    cout << "Nilai A Menurun = " << --a <<endl;

    return 0;

}
